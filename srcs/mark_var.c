#include "minishell.h"

void	mark_tilde(char **s, int *i, int quote, int dquote)
{
	if (s[0][*i] == '~' && (s[0][*i + 1] == '\0' || s[0][*i + 1] == '/'
	|| s[0][*i + 1] == ' ' || s[0][*i + 1] == ';' || s[0][*i + 1] == '|'
	|| s[0][*i + 1] == '<' || s[0][*i + 1] == '>') && (i == 0
	|| s[0][*i - 1] == ' ' || s[0][*i - 1] == ':' || s[0][*i - 1] == ';'
	|| s[0][*i - 1] == '|' || s[0][*i - 1] == '<' || s[0][*i - 1] == '>')
	&& quote == 0 && dquote == 0)
		s[0][*i] = '\xCF';
	return ;
}

void	mark_var(char **s, int *i, int quote, int dquote)
{
	int	j;

	j = *i + 1;
	if (s[0][*i] == '$' && ((s[0][*i + 1] >= '0' && s[0][*i + 1] <= '9')
	|| (s[0][*i + 1] >= 'a' && s[0][*i + 1] <= 'z') || (s[0][*i + 1] >= 'A'
	&& s[0][*i + 1] <= 'Z') || s[0][*i + 1] == '_' || s[0][*i + 1] == '?')
	&& quote == 0)
	{
		s[0][*i] = '\xCE';
		if (dquote == 1)
			s[0][*i] = '\xD1';
		if ((s[0][j] >= '0' && s[0][j] <= '9') || s[0][j] == '?')
			j = j + 1;
		else
		{
			while (s[0][j] != '\0' && ((s[0][j] >= '0' && s[0][j] <= '9')
			|| (s[0][j] >= 'a' && s[0][j] <= 'z') || (s[0][j] >= 'A'
			&& s[0][j] <= 'Z') || s[0][j] == '_'))
				j++;
		}
		s[0] = ft_insert_str(s[0], j, 0, ft_ctostr(1, '\xCE'));
	}
	return ;
}
